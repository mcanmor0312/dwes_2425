<?php
/**
 * tipos de variables
 */

#tipo booleano

$test=true;
echo"\$test: ";
var_dump(value: $test);
echo "<BR>";

// tipo int
$edad=50;
echo"\$edad";
var_dump(value: $edad);

// tipo float
$altura=1.70;
echo"\$altura";
var_dump(value: $altura);

#tipo string
$mensaje="La distancia recorrida fue de $distancia km";
echo"\$mensaje: ";
var_dump(value: $mensaje);

#tipo string
$mensaje="La distancia recorrida fue de .$distancia. km";
echo"\$mensaje: ";
var_dump(value: $mensaje);
